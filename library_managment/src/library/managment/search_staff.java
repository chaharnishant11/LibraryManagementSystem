    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String staffQuery = jTextField1.getText();
        String nameQuery = jTextField2.getText();
        
        DefaultListModel s1 = new DefaultListModel();
        s1.removeAllElements();
        lst.clear();
        try
        {
        int countT = 0;
        Class.forName("java.sql.DriverManager");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project","root", "1234");
        Statement stmt = con.createStatement();
        String q = "select * from issue_staff";
        if(nameQuery.length() > 0 || staffQuery.length() > 0)
            q += " where ";
        if(staffQuery.length() > 0)
        {
            q += "staffId='" + staffQuery + "' ";
            countT++;
        }
        if(nameQuery.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "name like '%" + nameQuery + "%' ";
            countT++;
        }
        q += ";";
        ResultSet rs = stmt.executeQuery(q);
        int countRS = 0;
        while(rs.next())
        {
            String str= rs.getString("staffId");
            lst.put(str, new String[]{
                rs.getString("name"), 
                rs.getString("address"),
                rs.getString("dob")
            });
            countRS++;
            s1.addElement(str);
        }
        if(countRS == 0)
        {
            JOptionPane.showMessageDialog(null, "NO RECORDS WERE FOUND");
            try
            {
                jList1.setModel(s1);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
        else
        {
            try
            {
                jList1.setModel(s1);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
            jLabel9.setText("");
            jLabel10.setText("");
            jLabel11.setText("");
            jLabel8.setText("");
        }
        catch (Exception x)
        {
            JOptionPane.showMessageDialog(this,x.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

