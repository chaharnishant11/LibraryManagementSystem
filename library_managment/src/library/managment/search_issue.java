    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String bookid = jTextField1.getText();
        String userid = jTextField2.getText();
        String staffid = jTextField3.getText();
        String status = (String)jComboBox1.getSelectedItem();
        DefaultListModel s1 = new DefaultListModel();
        s1.removeAllElements();
        lst.clear();
        try
        {
        int countT = 0;
        Class.forName("java.sql.DriverManager");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project","root", "1234");
        Statement stmt = con.createStatement();
        String q = "select issueId,issueDate,dueDate,fine,paymentDate,isFinePaid,isBookReturned from issues";
        if(bookid.length() > 0 || userid.length() > 0 || staffid.length() > 0 || (status.length() > 0 && !status.equals("ALL")))
            q += " where ";
        if(status.length() > 0)
        {
            if(status=="ISSUED"){
                q += "isBookReturned=0 ";
                countT++;
            }
            else if(status=="RETURNED") {
                q += "isBookReturned=1 ";
                countT++;
            }
         }
        if(bookid.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "bookId='" + bookid +"' ";
            countT++;
        }if(userid.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "userId='" + userid + "' ";
            countT++;
        }if(staffid.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "issueStaffId='" + staffid + "' ";
            countT++;
        }
        q += ";";
        ResultSet rs = stmt.executeQuery(q);
        int countRS = 0;
        while(rs.next())
        {
            String str= rs.getString("issueId");
            lst.put(str, new String[]{
                rs.getString("issueDate"), 
                rs.getString("dueDate"),
                rs.getString("fine"), 
                rs.getString("paymentDate"),
                rs.getString("isBookReturned"),
                rs.getString("isFinePaid")
            });
            countRS++;
            s1.addElement(str);
        }
        if(countRS == 0)
        {
            JOptionPane.showMessageDialog(null, "NO RECORDS WERE FOUND");
            try
            {
                jList1.setModel(s1);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
        else
        {
            try
            {
                jList1.setModel(s1);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
            jLabel12.setText("");
            jLabel13.setText("");
            jLabel14.setText("");
            jLabel15.setText("");
            jLabel16.setText("");
            jLabel17.setText("");
        }
        catch (Exception x)
        {
            JOptionPane.showMessageDialog(this,x.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        String issueId = jList1.getSelectedValue();
        if(issueId.length() > 0)
        {
            jLabel12.setText(issueId);
            jLabel13.setText(lst.get(issueId)[0]);
            jLabel14.setText(lst.get(issueId)[1]);
            jLabel15.setText(lst.get(issueId)[2]);
            jLabel16.setText(lst.get(issueId)[3] != null ? lst.get(issueId)[3] : "Not Applicable");
            if(lst.get(issueId)[4]=="1" && lst.get(issueId)[5] == "1" ){
                jLabel17.setText("Fine Paid / Fine N.A");
            }else if(lst.get(issueId)[4]=="1" && lst.get(issueId)[5] != "0"){
                jLabel17.setText("Fine Not Paid");
            }else{
                jLabel17.setText("Currently Issued");
            }
        }
        else
        {
            jLabel12.setText("");
            jLabel13.setText("");
            jLabel14.setText("");
            jLabel15.setText("");
            jLabel16.setText("");
            jLabel17.setText("");
        }
    }//GEN-LAST:event_jList1MouseClicked

