    public void display(){
        try
        {
        Class.forName("java.sql.DriverManager");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project","root", "1234");
        Statement stmt = con.createStatement();
        String q = "select * from issue_staff where staffId='"+uName+"';";
        ResultSet rs = stmt.executeQuery(q);
        int countRS = 0;
        while(rs.next())
        {
            jLabel6.setText(rs.getString("staffId"));
            jLabel7.setText(rs.getString("name")); 
            jLabel8.setText("address");
            jLabel10.setText(rs.getString("dob"));
            countRS++;
        }
        if(countRS == 0)
           JOptionPane.showMessageDialog(null, "NO RECORDS WERE FOUND");
        }
        catch (Exception x)
        {
            JOptionPane.showMessageDialog(this,x.getMessage());
        }
    }
