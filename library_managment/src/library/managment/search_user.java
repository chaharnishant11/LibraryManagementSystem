    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userQuery = jTextField1.getText();
        String nameQuery = jTextField2.getText();
        String roleQuery = jComboBox1.getSelectedItem().toString();
        if(roleQuery.equals("all"))
            roleQuery = "";
        DefaultListModel s1 = new DefaultListModel();
        s1.removeAllElements();
        lst.clear();
        try
        {
        int countT = 0;
        Class.forName("java.sql.DriverManager");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project","root", "1234");
        Statement stmt = con.createStatement();
        String q = "select * from lib_users";
        if(nameQuery.length() > 0 || userQuery.length() > 0 || roleQuery.length() > 0)
            q += " where ";
        if(userQuery.length() > 0)
        {
            q += "userId='" + userQuery + "' ";
            countT++;
        }
        if(nameQuery.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "name like '%" + nameQuery + "%' ";
            countT++;
        }
        if(roleQuery.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "userType='" + roleQuery + "'";
        }
        q += ";";
        ResultSet rs = stmt.executeQuery(q);
        int countRS = 0;
        while(rs.next())
        {
            String str= rs.getString("userId");
            lst.put(str, new String[]{
                rs.getString("name"), 
                rs.getString("dob"),
                rs.getString("address"), 
                rs.getString("userType")
            });
            countRS++;
            s1.addElement(str);
        }
        if(countRS == 0)
        {
            try
            {
                jList1.setModel(s1);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
            JOptionPane.showMessageDialog(null, "NO RECORDS WERE FOUND");
        }
        else
        {
            try
            {
                jList1.setModel(s1);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
            jTextField1.setText("");
            jTextField2.setText("");
            jLabel9.setText("");
            jLabel10.setText("");
            jLabel11.setText("");
            jLabel12.setText("");
            jLabel13.setText("");
        }
        catch (Exception x)
        {
            JOptionPane.showMessageDialog(this,x.getMessage());
        }                
    }//GEN-LAST:event_jButton1ActionPerformed

