    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String bookQuery = jTextField1.getText();
        String bnameQuery = jTextField2.getText();
        String bAuthorQuery = jTextField3.getText();
        String bPubQuery = jTextField4.getText();
        
        DefaultListModel s1 = new DefaultListModel();
        s1.removeAllElements();
        lst.clear();
        try
        {
        int countT = 0;
        Class.forName("java.sql.DriverManager");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project","root", "1234");
        Statement stmt = con.createStatement();
        String q = "select * from lib_book";
        if(bnameQuery.length() > 0 || bookQuery.length() > 0 || bAuthorQuery.length() > 0 || bPubQuery.length() > 0)
            q += " where ";
        if(bookQuery.length() > 0)
        {
            q += "bookId='" + bookQuery + "' ";
            countT++;
        }
        if(bnameQuery.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "bookName like '%" + bnameQuery + "%' ";
            countT++;
        }
        if(bAuthorQuery.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "authorName like '%" + bAuthorQuery + "%' ";
            countT++;
        }
        if(bPubQuery.length() > 0)
        {
            if(countT > 0)
                q += "AND ";
            q += "publisherName like '%" + bPubQuery + "%' ";
            countT++;
        }
        q += ";";
        ResultSet rs = stmt.executeQuery(q);
        int countRS = 0;
        while(rs.next())
        {
            String str= rs.getString("bookId");
            lst.put(str, new String[]{
                rs.getString("bookName"), 
                rs.getString("category"),
                rs.getString("pages"), 
                rs.getString("rackNumber"),
                rs.getString("authorName"),
                rs.getString("publisherName")
            });
            countRS++;
            s1.addElement(str);
        }
        if(countRS == 0)
        {
            JOptionPane.showMessageDialog(null, "NO RECORDS WERE FOUND");
            try
            {
                jList1.setModel(s1);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
           
        else
        {
            try
            {
                jList1.setModel(s1);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
            jLabel9.setText("");
            jLabel10.setText("");
            jLabel11.setText("");
            jLabel12.setText("");
            jLabel13.setText("");
            jLabel14.setText("");
            jLabel15.setText("");
        }
        catch (Exception x)
        {
            JOptionPane.showMessageDialog(this,x.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

