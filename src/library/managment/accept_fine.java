    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String issueId = jTextField1.getText();
        int isBookReturned = 1, isFinePaid = 1, fine = 0;
        String paymentDate = (String)jComboBox3.getSelectedItem() + "-" + (String)jComboBox2.getSelectedItem() + "-" + (String)jComboBox1.getSelectedItem(); 
        try
        {
            Class.forName("java.sql.DriverManager");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/DBMS_Project","root", "1234");
            Statement stmt = con.createStatement();
            String q ="select isBookReturned, isFinePaid, fine from issues where issueId= '"+issueId+"';";
            ResultSet rs = stmt.executeQuery(q);
            int countRS = 0;
            while(rs.next())
            {
                countRS++;
                isBookReturned = rs.getInt("isBookReturned");
                isFinePaid = rs.getInt("isFinePaid");
                fine = rs.getInt("fine");
            }
            if(countRS != 1)
            {
                JOptionPane.showMessageDialog(null, "No Records Found");
            }
            else if(isFinePaid == 1)
                JOptionPane.showMessageDialog(null, "Fine has already been paid");
            else if(isBookReturned == 0)
                JOptionPane.showMessageDialog(null, "Book has not been returned");
            else
            {
                q = "update issues set isFinePaid = 1, fineStaffId = '"+staffId+"', paymentDate='"+paymentDate+"' where issueId="+issueId+";";
                stmt.executeUpdate(q);
                JOptionPane.showMessageDialog(null, "A fine of "+fine+ " has been collected");
            }
        }
        catch (Exception x)
        {
            JOptionPane.showMessageDialog(this,x.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

